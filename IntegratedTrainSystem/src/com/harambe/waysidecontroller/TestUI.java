    private void jToggleButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton3ActionPerformed
        // TODO add your handling code here:
        String curSwitch = String.valueOf(jComboBox3.getSelectedItem());
        int curSwitchNum = Integer.parseInt(curSwitch);
        for(SwitchBlock sb: test.wc.switches){
            if(sb.switchNum == curSwitchNum){
                sb.toggleLight();
                if(sb.light){
                    jTextField2.setText("On");
                }
                else {
                    jTextField2.setText("Off");
                }
            }
        }
    }//GEN-LAST:event_jToggleButton3ActionPerformed

