    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed
        // TODO add your handling code here:
        //System.out.println(evt.getActionCommand());
        String curSwitch = String.valueOf(jComboBox3.getSelectedItem());
        int curSwitchNum = Integer.parseInt(curSwitch);
        SwitchBlock sb = test.wc.getSwitch(curSwitchNum);
        if(sb.light){
            jTextField2.setText("On");
        }
        else {
            jTextField2.setText("Off");
        }
        
        jTextField1.setText(sb.getCurrentPosition().toString());
        
        jTextField3.setText(sb.blockNumber + "");
    }//GEN-LAST:event_jComboBox3ActionPerformed

